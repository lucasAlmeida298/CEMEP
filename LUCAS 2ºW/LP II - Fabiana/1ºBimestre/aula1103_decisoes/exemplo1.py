#Exemplo de programa utilizando estrutura de decisão simples onde temos apenas uma resposta
"""Exemplo: Faça um programa que receba a média de um aluno e verifique
se ele foi aprovado. (aprovado média >=6 e media <=10)"""

media = float(input("Digite a média do aluno: "))
if (media>=6 and media<=10):
    print ("Aluno aprovado")
    #fim da estrutura de decisão

print ("Fim do programa")
