#função type: usada para verificar o tipo de uma variável em Python
a = 10
b = 1.5
nome = ("Lucas")
print ("O tipo da variável a é: ",type(a))
#__name__ é uma variável especial que indica o nome do módulo que está sendo executado
print ("O tipo de variável a é: ",type(a).__name__)
print ("O tipo de variável b é: ",type(b).__name__)
print ("O tipo de variável nome é: ",type(nome).__name__)
