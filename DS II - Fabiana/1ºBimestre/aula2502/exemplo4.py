"""Faça um programa que receba o nome, idade e peso de uma pessoa e
exiba uma mensagem conforme o exemplo:
Lucas tem 25 anos e pesa 70.5 quilos"""
#entrada
nome = "Lucas"
idade = 25
peso = 70.5
#processamento/saída
#exibindo apenas as varáveis
