#Função print: usada para exibir informações na tela.
#Abaixo alguns exemplos de utilização da função print
#exibindo um texto na tela
print ("Bom dia")
a = 10
b = 30
#exibindo o contúdo de uma variável
print (a)
print (b)
print (a, b)
#exibindo uma mensagem junto com uma variável
print ("O valor da variável a é: ", a)
print ("O valor da variável b é: ", b)
#outros exemplos de utilização
print (10 + 30)
print (a + b)
print ("A soma do 10 + 30 é: ", 10 + 30)
print ("A soma do a + b é: ", a + b)
